<script>
$(function() {

    /*
    |--------------------------------------------------------------------------
    | Buka Sidebar
    |--------------------------------------------------------------------------
    */

    $("#menu-toggle").click(function() {

        $("#sidebar").addClass("show");

        $("#sidebar-overlay").fadeIn(200);

    });

    /*
    |--------------------------------------------------------------------------
    | Tutup Sidebar
    |--------------------------------------------------------------------------
    */

    $("#sidebar-overlay").click(function() {

        $("#sidebar").removeClass("show");

        $(this).fadeOut(200);

    });

});
</script>